    public void update_penumpang(Penumpang penumpang) {
        this.penumpang = penumpang;
        txtIdPenumpang.setText(Integer.toString(penumpang.getId()));
        txtKodePenumpang.setText(penumpang.getKode_customer());
        txtNamaPenumpang.setText(penumpang.getNama_penumpang());
        String jenisKelamin = "";
        if(rdLaki.isSelected()) {
            jenisKelamin = rdLaki.getText();
        } else {
            jenisKelamin = rdLaki.getText();
        }
        txtNoHP.setText(penumpang.getNo_hp());
        txtAlamat.setText(penumpang.getAlamat());
        setVisible(true);
        setLocationRelativeTo(getParent());
    }
    
