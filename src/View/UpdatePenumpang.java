    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        this.penumpang = penumpang;
        penumpang.setKode_customer(txtKodePenumpang.getText());
        penumpang.setNama_penumpang(txtNamaPenumpang.getText());
        if(rdLaki.isSelected()) {
            penumpang.setJenis_kelamin("Laki-laki");
        } else {
            penumpang.setJenis_kelamin("Perempuan");
        }
        penumpang.setNo_hp(txtNoHP.getText());
        penumpang.setAlamat(txtAlamat.getText());
        
        PenumpangController controller = new PenumpangController();
        controller.Update_Penumpang(penumpang);
        dispose();
        JOptionPane.showMessageDialog(null, "Penumpang berhasil di ubah");
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void txtAlamatKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAlamatKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            // cek inputan
            if(txtAlamat.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Alamat tidak boleh kosong", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                btnSimpan.doClick();
            }
        }
    }//GEN-LAST:event_txtAlamatKeyPressed

