    private void txtUsernameKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsernameKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            // cek inputan
            if(txtUsername.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Username", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                txtPassword.requestFocus();
            }
        }
    }//GEN-LAST:event_txtUsernameKeyPressed

    private void txtPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            // cek inputan kosong
            if(txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Password tidak boleh kosong", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                txtNama.requestFocus();
            }
        }
    }//GEN-LAST:event_txtPasswordKeyPressed

    private void txtNamaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNamaKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            // cek inputan kosong
            if(txtNama.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nama petugas tidak boleh kosong", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                btnSimpan.doClick();
            }
        }
    }//GEN-LAST:event_txtNamaKeyPressed

