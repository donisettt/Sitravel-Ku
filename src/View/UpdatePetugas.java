    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        this.petugas = petugas;
        petugas.setUsername(txtUsername.getText());
        petugas.setPassword(txtPassword.getText());
        petugas.setNama_petugas(txtNama.getText());
        
        PetugasController controller = new PetugasController();
        controller.Update_Petugas(petugas);
        dispose();
        JOptionPane.showMessageDialog(this, "Petugas Berhasil Di Update!");
    }//GEN-LAST:event_btnSimpanActionPerformed

